@frappe.whitelist()
def seed_extended(force: int = 0) -> dict:
    """Создаёт расширенный набор шаблонов."""
    if "System Manager" not in frappe.get_roles(frappe.session.user):
        frappe.throw("Только System Manager", frappe.PermissionError)

    created = 0
    skipped = 0
    updated = 0
    for tpl_data in EXTENDED_TEMPLATES:
        tid = tpl_data["template_id"]
        exists = frappe.db.exists("Work Template", tid)

        if exists and not int(force or 0):
            skipped += 1
            continue

        if exists:
            frappe.delete_doc("Work Template", tid, force=True, ignore_permissions=True)
            updated += 1

        doc = frappe.get_doc({
            "doctype": "Work Template",
            "template_id": tid,
            "title": tpl_data["title"],
            "category": tpl_data["category"],
            "base_unit": tpl_data["base_unit"],
            "typical_volume_min": tpl_data["typical_volume_min"],
            "typical_volume_max": tpl_data["typical_volume_max"],
            "keywords": tpl_data["keywords"],
            "description": tpl_data["description"],
            "source": "Ручной ввод",
            "is_verified": 1,
            "stages": [
                {**s, "stage_order": idx}
                for idx, s in enumerate(tpl_data["stages"], start=1)
            ],
        })
        doc.insert(ignore_permissions=True)
        if not exists:
            created += 1

    frappe.db.commit()
    return {
        "ok": True,
        "created": created,
        "updated": updated,
        "skipped": skipped,
        "total": len(EXTENDED_TEMPLATES),
    }
